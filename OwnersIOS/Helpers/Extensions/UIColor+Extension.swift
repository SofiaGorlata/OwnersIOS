import UIKit

extension UIColor {
    class var darkPurple: UIColor {
        return #colorLiteral(red: 0.1254901961, green: 0.1960784314, blue: 0.6235294118, alpha: 1)
    }
    
    class var purpleBlue: UIColor {
        return #colorLiteral(red: 0.2078431373, green: 0.3137254902, blue: 0.8901960784, alpha: 1)
    }
    
    class var lightPurple: UIColor {
        return #colorLiteral(red: 0.8235294118, green: 0.8549019608, blue: 0.9960784314, alpha: 1)
    }
    
    class var lightSand: UIColor {
        return #colorLiteral(red: 0.9960784314, green: 0.8352941176, blue: 0.7764705882, alpha: 1)
    }
    
    class var sand: UIColor {
        return #colorLiteral(red: 0.9921568627, green: 0.737254902, blue: 0.6549019608, alpha: 1)
    }
}
